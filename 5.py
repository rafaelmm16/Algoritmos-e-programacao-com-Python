temperaturas = [-10, -5, 0, 1, 3, 5, -6, -8]

# Encontrando a menor temperatura
menor_temperatura = min(temperaturas)

# Encontrando a maior temperatura
maior_temperatura = max(temperaturas)

# Calculando a média das temperaturas
soma_temperaturas = sum(temperaturas)
quantidade_temperaturas = len(temperaturas)
media_temperaturas = soma_temperaturas / quantidade_temperaturas

# Imprimindo os resultados
print("A menor temperatura é:", menor_temperatura)
print("A maior temperatura é:", maior_temperatura)
print("A temperatura média é:", media_temperaturas)